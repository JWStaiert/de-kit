TEST( de__exception , runtime_error_with_cstring_throws )
{
	try
	{
		DE_THROW_RUNTIME_ERROR( "cstring" );

		FAIL( ) << "Expected exception.";
	}
	catch ( std::runtime_error & exc )
	{
		EXPECT_EQ( exc.what( ) , std::string( "de__exception_runtime_error_with_cstring_throws_Test::TestBody @ 13 | cstring" ) );
	}
	catch ( ... )
	{
		FAIL( ) << "Unexpected exception.";
	}
}

